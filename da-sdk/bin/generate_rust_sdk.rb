#!/usr/bin/env ruby
# frozen_string_literal: true

require "json"
require "net/http"
require "optparse"
require "time"
require "uri"
require "fileutils"

class RustSdkGenerator
  RUST_KEYWORDS = %w[
    as break const continue crate else enum extern false fn for if impl in let loop
    match mod move mut pub ref return self Self static struct super trait true type unsafe
    use where while async await dyn abstract become box do final macro override priv try typeof
    unsized virtual yield
  ].freeze

  def initialize(catalog:, output_path:)
    @catalog = catalog
    @output_path = output_path
  end

  def write!
    FileUtils.mkdir_p(File.dirname(@output_path))
    File.write(@output_path, render)
  end

  private

  def render
    actions = Array(@catalog["actions"])
    generated_at = @catalog["generated_at"] || Time.now.utc.iso8601

    body = []
    body << "// This file is generated by da-sdk/bin/generate_rust_sdk.rb"
    body << "// Source catalog generated_at: #{generated_at}"
    body << ""
    body << "#[derive(Debug, Clone, serde::Serialize, serde::Deserialize)]"
    body << "pub struct PendingRequest {"
    body << "    pub action_slug: &'static str,"
    body << "    pub params_json: Vec<u8>,"
    body << "}"
    body << ""
    body << "#[derive(Debug)]"
    body << "pub enum ClientError {"
    body << "    Serialize(serde_json::Error),"
    body << "    Deserialize(serde_json::Error),"
    body << "    UnknownActionSlug(String),"
    body << "}"
    body << ""
    body << "#[cfg(feature = \"anchor\")]"
    body << "#[derive(Debug)]"
    body << "pub enum AnchorEmitError {"
    body << "    Client(ClientError),"
    body << "    CounterOverflow,"
    body << "}"
    body << ""
    body << "#[cfg(feature = \"anchor\")]"
    body << "impl From<ClientError> for AnchorEmitError {"
    body << "    fn from(err: ClientError) -> Self {"
    body << "        Self::Client(err)"
    body << "    }"
    body << "}"
    body << ""
    body << "#[cfg(feature = \"anchor\")]"
    body << "use anchor_lang::{event, AnchorDeserialize, AnchorSerialize, Discriminator};"
    body << "#[cfg(all(feature = \"anchor\", feature = \"idl-build\"))]"
    body << "use anchor_lang::IdlBuild;"
    body << "#[cfg(feature = \"anchor\")]"
    body << "use anchor_lang::prelude::borsh;"
    body << ""
    body << "#[cfg(feature = \"anchor\")]"
    body << "pub trait HasRequestCounter {"
    body << "    fn request_counter_mut(&mut self) -> &mut u64;"
    body << "}"
    body << ""
    body << "#[cfg(feature = \"anchor\")]"
    body << "impl<'info, T> HasRequestCounter for anchor_lang::prelude::Account<'info, T>"
    body << "where"
    body << "    T: HasRequestCounter + anchor_lang::AccountSerialize + anchor_lang::AccountDeserialize + Clone,"
    body << "{"
    body << "    fn request_counter_mut(&mut self) -> &mut u64 {"
    body << "        (&mut **self).request_counter_mut()"
    body << "    }"
    body << "}"
    body << ""
    body << "#[cfg(feature = \"anchor\")]"
    body << "#[event]"
    body << "pub struct ActionRequested {"
    body << "    pub request_id: u64,"
    body << "    pub action_slug: String,"
    body << "    pub params_json: Vec<u8>,"
    body << "}"
    body << ""
    body << "pub struct OracleClient;"
    body << ""
    body << "impl Default for OracleClient {"
    body << "    fn default() -> Self {"
    body << "        Self"
    body << "    }"
    body << "}"
    body << ""

    actions.each do |action|
      slug = action.fetch("slug")
      base_name = pascalize(slug)
      body << render_schema_struct("#{base_name}Params", action["request_schema"])
      body << ""
      body << render_schema_struct("#{base_name}Response", action["response_schema"])
      body << ""
    end

    body << "#[derive(Debug, Clone)]"
    body << "pub enum TypedOnCallParams {"
    actions.each do |action|
      slug = action.fetch("slug")
      variant = pascalize(slug)
      body << "    #{variant}(#{variant}Params),"
    end
    body << "}"
    body << ""
    body << "impl TypedOnCallParams {"
    body << "    pub fn action_slug(&self) -> &'static str {"
    body << "        match self {"
    actions.each do |action|
      slug = action.fetch("slug")
      variant = pascalize(slug)
      body << "            Self::#{variant}(_) => \"#{slug}\","
    end
    body << "        }"
    body << "    }"
    body << ""
    body << "    pub fn from_on_call(action_slug: &str, params_json: &[u8]) -> Result<Self, ClientError> {"
    body << "        match action_slug {"
    actions.each do |action|
      slug = action.fetch("slug")
      variant = pascalize(slug)
      body << "            \"#{slug}\" => serde_json::from_slice::<#{variant}Params>(params_json)"
      body << "                .map(Self::#{variant})"
      body << "                .map_err(ClientError::Deserialize),"
    end
    body << "            _ => Err(ClientError::UnknownActionSlug(action_slug.to_string())),"
    body << "        }"
    body << "    }"
    body << ""
    body << "    pub fn into_pending_request(self) -> Result<PendingRequest, ClientError> {"
    body << "        let client = OracleClient::default();"
    body << "        match self {"
    actions.each do |action|
      slug = action.fetch("slug")
      variant = pascalize(slug)
      method_name = "#{snake_case(slug)}_on_call"
      body << "            Self::#{variant}(params) => client.#{safe_ident(method_name)}(params),"
    end
    body << "        }"
    body << "    }"
    body << "}"
    body << ""

    actions.each do |action|
      slug = action.fetch("slug")
      variant = pascalize(slug)
      params_type = "#{variant}Params"
      method_name = "#{snake_case(slug)}_on_call"
      emit_fn = "call_#{snake_case(slug)}_action"
      emit_from_fn = "#{emit_fn}_from"

      body << "#[cfg(feature = \"anchor\")]"
      body << "pub fn #{safe_ident(emit_fn)}("
      body << "    request_counter: &mut u64,"
      body << "    params: #{params_type},"
      body << ") -> Result<u64, AnchorEmitError> {"
      body << "    let pending = OracleClient::default()"
      body << "        .#{safe_ident(method_name)}(params)"
      body << "        .map_err(AnchorEmitError::Client)?;"
      body << "    let request_id = *request_counter;"
      body << "    *request_counter = request_id.checked_add(1).ok_or(AnchorEmitError::CounterOverflow)?;"
      body << "    anchor_lang::prelude::emit!(ActionRequested {"
      body << "        request_id,"
      body << "        action_slug: \"#{slug}\".to_string(),"
      body << "        params_json: pending.params_json,"
      body << "    });"
      body << "    Ok(request_id)"
      body << "}"
      body << ""
      body << "#[cfg(feature = \"anchor\")]"
      body << "pub fn #{safe_ident(emit_from_fn)}<S: HasRequestCounter>("
      body << "    state: &mut S,"
      body << "    params: #{params_type},"
      body << ") -> Result<u64, AnchorEmitError> {"
      body << "    #{safe_ident(emit_fn)}(state.request_counter_mut(), params)"
      body << "}"
      body << ""
    end

    body << "impl OracleClient {"
    actions.each do |action|
      slug = action.fetch("slug")
      method_name = "#{snake_case(slug)}_on_call"
      params_type = "#{pascalize(slug)}Params"

      body << "    pub fn #{safe_ident(method_name)}(&self, params: #{params_type}) -> Result<PendingRequest, ClientError> {"
      body << "        let params_json = serde_json::to_vec(&params).map_err(ClientError::Serialize)?;"
      body << "        Ok(PendingRequest {"
      body << "            action_slug: \"#{slug}\","
      body << "            params_json,"
      body << "        })"
      body << "    }"
      body << ""
    end
    body << "}"

    body.join("\n") + "\n"
  end

  def render_schema_struct(name, schema)
    schema_hash = schema.is_a?(Hash) ? schema : {}
    type = schema_hash["type"]
    properties = schema_hash["properties"]

    unless type == "object" && properties.is_a?(Hash)
      rust_type = rust_type_for_schema(schema_hash)
      return <<~RUST.chomp
        #[derive(Debug, Clone, serde::Serialize, serde::Deserialize)]
        pub struct #{name} {
            pub value: #{rust_type},
        }
      RUST
    end

    required = Array(schema_hash["required"]).map(&:to_s)
    lines = []
    lines << "#[derive(Debug, Clone, serde::Serialize, serde::Deserialize)]"
    lines << "pub struct #{name} {"

    properties.each do |prop_name, prop_schema|
      field_name = safe_ident(snake_case(prop_name.to_s))
      rust_type = rust_type_for_schema(prop_schema)
      is_required = required.include?(prop_name.to_s)
      field_type = is_required ? rust_type : "Option<#{rust_type}>"

      lines << "    #[serde(rename = \"#{prop_name}\")]" if field_name != prop_name
      lines << "    #[serde(skip_serializing_if = \"Option::is_none\")]" unless is_required
      lines << "    pub #{field_name}: #{field_type},"
    end

    lines << "}"
    lines.join("\n")
  end

  def rust_type_for_schema(schema)
    hash = schema.is_a?(Hash) ? schema : {}
    type = hash["type"]

    if type.is_a?(Array)
      simple = type.reject { |entry| entry == "null" }
      return "serde_json::Value" if simple.length != 1

      inner = rust_type_for_schema(hash.merge("type" => simple.first))
      return type.include?("null") ? "Option<#{inner}>" : inner
    end

    case type
    when "string"
      "String"
    when "integer"
      "i64"
    when "number"
      "f64"
    when "boolean"
      "bool"
    when "array"
      item_type = rust_type_for_schema(hash["items"])
      "Vec<#{item_type}>"
    when "object"
      "serde_json::Value"
    else
      "serde_json::Value"
    end
  end

  def pascalize(text)
    snake_case(text).split("_").reject(&:empty?).map(&:capitalize).join
  end

  def snake_case(text)
    text.to_s.downcase.gsub(/[^a-z0-9]+/, "_").gsub(/\A_+|_+\z/, "")
  end

  def safe_ident(name)
    ident = name
    ident = "value" if ident.empty?
    ident = "_#{ident}" if RUST_KEYWORDS.include?(ident)
    ident
  end
end

options = {
  catalog_url: ENV.fetch("DAMHCHUIRE_CATALOG_URL", "http://127.0.0.1:3000/sdk/catalog.json"),
  output: File.expand_path("../rust/src/generated/actions.rs", __dir__)
}

OptionParser.new do |parser|
  parser.banner = "Usage: generate_rust_sdk.rb [options]"

  parser.on("--catalog-url URL", "Backend catalog URL") do |url|
    options[:catalog_url] = url
  end

  parser.on("--output PATH", "Output Rust file path") do |path|
    options[:output] = File.expand_path(path)
  end
end.parse!

uri = URI.parse(options[:catalog_url])
catalog = if uri.scheme == "http" || uri.scheme == "https"
  response = Net::HTTP.get_response(uri)
  unless response.is_a?(Net::HTTPSuccess)
    raise "failed to fetch catalog: HTTP #{response.code}"
  end

  JSON.parse(response.body)
elsif uri.scheme == "file"
  JSON.parse(File.read(uri.path))
elsif File.exist?(options[:catalog_url])
  JSON.parse(File.read(options[:catalog_url]))
else
  raise "unsupported catalog URL or path: #{options[:catalog_url]}"
end

RustSdkGenerator.new(catalog: catalog, output_path: options[:output]).write!
puts "Wrote #{options[:output]}"
